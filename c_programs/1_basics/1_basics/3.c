// 3. Program to accept values of two numbers and print their addition.

#include<stdio.h>


int main()
{
  int a,b,ans;

  printf("Enter 1st number: ");
  scanf("%d",&a);

  printf("Enter 2nd number: ");
  scanf("%d",&b);

  ans = a + b;

  printf("Addition of two numbers is: %d",ans);

return 0;
}
